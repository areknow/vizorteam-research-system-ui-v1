<h2>Frequently Asked Questions</h2>
<hr>
<div id="accordian-wrapper" class="forms">
    <div class="accordionButton">
I just created an account, now what?
    </div>
    <div class="accordionContent">
Go to the Edit Profile tab to input your name, email, and telephone number, the to the Research tab to specify your areas of expertise, desired keywords, and the URL's that will serve as the starting point for the search.  After that, you simply sit back and let the crawler do the work.  You should receive an email every week with the top matches.
    </div>
    <div class="accordionButton">
How do I add keywords and URLs?
    </div>
    <div class="accordionContent">
Click on the Research tab, you will see your current list of keywords and URLs (which will be blank at first).  To add a new entry to either field, type the desired text into the text box and hit space or the enter key to enter another.  When you are done on this page, make sure you click the Save button at the top of the page to submit your changes.
    </div>
    <div class="accordionButton">
Where can I see my results?
    </div>
    <div class="accordionContent">
The PDFs that the crawler finds will show up in the Proposals tab, as well as on the your Dashboard tab.  The top results will be listed in order of relevance.
    </div>
    <div class="accordionButton">
Can I give feedback on the results I'm getting?
    </div>
    <div class="accordionContent">
You certainly can! Next to each result on the Proposals tab, you will see a green check mark and a red X.  If you found that a specific proposal was very helpful, click the green check mark.  If it was unsatisfactory, click the red X.  The crawler will take your feedback into account in future searches. (feature coming soon)
    </div>
    <div class="accordionButton">
How do I start/stop getting emails and texts from this site?
    </div>
    <div class="accordionContent">
Go to the Contact tab and select the appropriate check boxes for email and text messaging.
    </div>
    <div class="accordionButton">
How do I change my password?
    </div>
    <div class="accordionContent">
In the Edit Profile tab, simply input your desired new password into the "New Password" and "Confirm Password" text boxes and click the Save button at the top of the page.
    </div>
    <div class="accordionButton">
Can I share my results with other professors and students?
    </div>
    <div class="accordionContent">
This feature is currently in the works.  You will be able to share your results with colleagues, students and other interested personnel via email, social networks such as LinkedIn or Twitter, or through our own website.
    </div>
    <div class="accordionButton">
How can I get the best results?
    </div>
    <div class="accordionContent">
There are several ways you can maximize the effectiveness of the crawler.  First, make sure your starter URLs are fairly general (for example www.nasa.gov or www.mit.edu).  The more you provide, the more variety you are likely to get.  Secondly, avoid general keywords such as "robot" or "electronics", and use more specific terms such as (insert specific keywords here).  And lastly, give the crawler feedback on the results it gives you.  Feedback can be submitted on the your home page.  Next to each result you receive, click the thumbs up or thumbs down buttons to indicate whether the opportunity was helpful or not.
    </div>
</div>